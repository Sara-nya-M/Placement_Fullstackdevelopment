package com.example.study;
public class Student {
    private String name;
    private int age ;
    private int mark;
    public String getName(){
        return name;
    }
    public void setName(String name){
        this.name = name;
    }
    public int getAge(){
        return age;
    }
    public void setAge(int age){
        this.age = age;
    }
    public int getMark(){
        return mark;
    }
    public void setMark(int mark){
        this.mark = mark;
    }
}
